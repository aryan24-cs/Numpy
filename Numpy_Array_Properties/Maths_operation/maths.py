import numpy as np

arr = np.array([10 ,20, 30, 40, 50])

#to add 5 to each element
print("Addition:", arr + 5)

#to subtract 5 from each element
print("Subtraction:", arr - 5)

#to multiply each element by 5
print("Multiplication:", arr * 5)

#to divide each element by 5    
print("Division:", arr / 5)

#to find the square root of each element
print("Square Root:", np.sqrt(arr))

#to find the square of each element
print("Square:", np.square(arr))

#to calculate the power 2 times of each element
print("Power 2:", arr ** 2)